public class RecCommentFriend {
    private String company;
    private String userheadlogo;
    private String sign;
    private String recmdStr;
    private String taglist;
    private String nickname;
    private String userid;

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getUserheadlogo() {
        return userheadlogo;
    }

    public void setUserheadlogo(String userheadlogo) {
        this.userheadlogo = userheadlogo;
    }

    public String getSign() {
        return sign;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }

    public String getRecmdStr() {
        return recmdStr;
    }

    public void setRecmdStr(String recmdStr) {
        this.recmdStr = recmdStr;
    }

    public String getTaglist() {
        return taglist;
    }

    public void setTaglist(String taglist) {
        this.taglist = taglist;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }
}
